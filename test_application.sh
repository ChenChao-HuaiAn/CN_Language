#!/bin/bash

# CN_Language 应用层测试脚本
# 版本: 1.0.0
# 作者: CN_Language架构委员会
# 日期: 2026-01-09

set -e

echo "========================================="
echo "CN_Language 应用层测试"
echo "========================================="

# 检查是否在项目根目录
if [ ! -f "Makefile" ]; then
    echo "错误：请在项目根目录运行此脚本"
    exit 1
fi

# 清理之前的构建
echo "1. 清理构建文件..."
make clean

# 构建项目
echo "2. 构建项目..."
make

# 检查可执行文件是否存在
if [ ! -f "bin/CN_Language" ]; then
    echo "错误：构建失败，可执行文件未生成"
    exit 1
fi

echo "3. 测试帮助命令..."
echo "-----------------------------------------"
./bin/CN_Language help
echo "-----------------------------------------"

echo "4. 测试版本命令..."
echo "-----------------------------------------"
./bin/CN_Language version
echo "-----------------------------------------"

echo "5. 测试编译命令..."
echo "-----------------------------------------"
./bin/CN_Language compile test.cn
echo "-----------------------------------------"

echo "6. 测试运行命令..."
echo "-----------------------------------------"
./bin/CN_Language run test.cn
echo "-----------------------------------------"

echo "7. 测试调试命令..."
echo "-----------------------------------------"
./bin/CN_Language debug test.cn
echo "-----------------------------------------"

echo "8. 测试未知命令..."
echo "-----------------------------------------"
./bin/CN_Language unknown_command
echo "-----------------------------------------"

echo "9. 测试无参数情况..."
echo "-----------------------------------------"
./bin/CN_Language
echo "-----------------------------------------"

echo "10. 使用Makefile测试目标..."
echo "-----------------------------------------"
make test-help
echo "-----------------------------------------"
make test-version
echo "-----------------------------------------"
make test-compile
echo "-----------------------------------------"
make test-run
echo "-----------------------------------------"
make test-debug
echo "-----------------------------------------"

echo "========================================="
echo "测试完成！"
echo "所有测试已成功执行。"
echo "========================================="

# 显示构建信息
echo ""
echo "构建信息："
make info
